const mongoose = require('mongoose');
const studentSchema = new mongoose.Schema({
    username: {
        type: String,
        required: true,
    },
    email: {
        type: String,
        required: true,
        unique: true
    },
    phonenumber: {
        type: Number,
        required: true,
        unique: true
    },
    password: {
        type: String,
        required: true
    },
    confirmpassword: {
        type: String,
        required: true
    },
    address: {
        type: String,
        required: true
    }
});

const Register = new mongoose.model("Student", studentSchema);

module.exports = Register;